    public void EnviarModelo(){
        JFileChooser jfc = escolherArquivo();
        int resultado = jfc.showOpenDialog(this);
        if(resultado == JFileChooser.APPROVE_OPTION){
            try{
                
                setFis(new FileInputStream(jfc.getSelectedFile()));
                String mens = JOptionPane.showInputDialog("Escreve uma mensagem curta:");
                String nametipo = jfc.getSelectedFile().getName();
                String tipo = nametipo.substring(nametipo.lastIndexOf("."));
                if(mens != null && !mens.isEmpty()){
                    Carregando tela = new Carregando();
                    tela.CriandoModelo(tela, getUser().getId(), mens, getFis(),tipo);
                }
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Houve um erro ao enviar! " + e.getMessage() + ".");
            }
        }
    }
    
    public JFileChooser escolherArquivo(){
        JFileChooser jfc = new JFileChooser();
        jfc.setDialogTitle("Selecionar arquivo");
        jfc.setFileFilter(new FileNameExtensionFilter("Arquivos (*.PDF,*.STL,*.PNG,*.JPG,*.JPEG)","pdf", "stl", "png", "jpg", "jpeg"));
        return jfc;
    }
    
