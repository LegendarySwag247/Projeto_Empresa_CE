    public void ColocarIcone(){
        String caminho = "/image/logo.jpg";
        InputStream pasta = getClass().getResourceAsStream(caminho);
        try{
            if( pasta != null){
                Image imagem = new ImageIcon(ImageIO.read(pasta)).getImage();
                this.setIconImage(imagem);
            }else{
                System.out.println("Imagem Nao encontrado!");
            }
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public ImageIcon converterByteEmImage(byte[] image){
        try{
            ByteArrayInputStream bis = new ByteArrayInputStream(image);
            BufferedImage bufferedImage = ImageIO.read(bis);
            Image imagemScale = bufferedImage.getScaledInstance(imagePerfil.getWidth(), imagePerfil.getHeight(), Image.SCALE_SMOOTH);
            return new ImageIcon(imagemScale);
        }catch(Exception e){
            return null;
        }
    }
    
    public void AbrirClienteInicial(InicialCliente tela, Usuario user){
        tela.setVisible(true);
        labelCliente.setText(user.getNome() + "!");
        ImageIcon imageP = converterByteEmImage(user.getImagem());
        imagePerfil.setIcon(imageP);
        setUser(user);
    }
    
